public class question6_caffeine {

	public static void main(String[] args) {
		
		int milligramCaffeine;
		int drink;
		
		milligramCaffeine=500;
		drink=10000/milligramCaffeine;
		
		System.out.println("Number of milligrams in drink: " + milligramCaffeine);
		System.out.println("It would take about " + drink + " drinks for a lethal overdose.");

	}

}
