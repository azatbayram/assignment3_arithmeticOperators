package assignment3_arithmeticOperators;

public class question4_swapNumber {

	public static void main(String[] args) {
		
		int num1=10;
		int num2=20;
		int num;
		
		System.out.println("n1= " + num1 + "\nn2= " + num2);
		System.out.println("-------------------------------");
		
		num=num1;
		num1=num2;
		num2=num;
		
		System.out.println("n1= " + num1 + "\nn2= " + num2);
		

	}

}
